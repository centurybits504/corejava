package Keywords;
/*Character Example */
public class CharKeyWordExample {
	public static void main(String[] args) {
		char char1 = 'A';
		char char2 = 65;	/*UTF-16 (decimal)*/
		char char3 = '\u0041';	/*Unicode*/
		
		char char4 = 'a';
		char char5 = 97;	/*UTF-16 (decimal)*/
		char char6 = '\u0061';	/*Unicode*/
		System.out.println(char1);
		System.out.println(char2);
		System.out.println(char3);
		
		System.out.println(char4);
		System.out.println(char5);
		System.out.println(char6);

	}
}


